# Operadores Relacionais
# Igual a          : '=='
# Diferente de     : '!='
# Maior que        : '>'
# Menor que        : '<'
# Maior ou igual a : '>='
# Menor ou igual a : '<='

# Comparações retornam True ou False (Valores Booleanos)

print(3 < 2) # False
print('Davi' == 'Davi') # True
print('Ricardo' == 'ricardo') # False
print(10 != 2) # True
print(10 != 10) # False